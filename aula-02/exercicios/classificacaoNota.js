let nota = 5

// Nota é igual a 10 -> Excelente
// Nota maior que 7 -> Aprovado
// Nota entre 5,6 e 7 -> Recuperação
// NOta menor que 5 -> Reprovado

if(nota == 10){
    console.log("Excelente")
}else if (nota >= 7){
    console.log("Bom")
}else if (nota >= 5){
    console.log("Regular")
}else{
    console.log("Insuficiente")
}